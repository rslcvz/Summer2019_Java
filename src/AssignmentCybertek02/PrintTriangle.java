package AssignmentCybertek02;

import java.util.Scanner;

public class PrintTriangle {

	public static void main(String[] args) {

		
		Scanner scan = new Scanner(System.in);
		int n = scan.nextInt();
		for(int j=0;  j <n ; j++) {
			
		for(int i=0;  i <= j; i++) {
		System.out.print("*");
		       
		}
		System.out.println();
  }
}
	}


