package AssignmentCybertek02;

public class LoopsAsiyaPractice {

	public static void main(String[] args) {
		int count = 0;
		for (int i = 0; i <= 100; i += 2) {
			System.out.println(i);
		}
		for (int i = 100; i >= 97; i--) {
			System.out.print(i + " ");

		}
		for (int i = 0; i <= 100; i++) {
			if (i % 2 != 0) {
				System.out.println(i + " ");

			}

		}
	}
}