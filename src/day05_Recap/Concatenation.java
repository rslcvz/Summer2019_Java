package day05_Recap;

public class Concatenation {

	public static void main(String[] args) {

		/*
		 
		  String: Represents the sequences of characters, used for storing text. String values are surrendered by a double quote.
		  Concatenation: combining, linking things together ==> creates a string value
		 */
		String str = "any text goes here";
		System.out.println(str);
		
		String str2 = "1";
		System.out.println(str2);
		System.out.println("Hello World");
		
		String Hello = "Hello World";
		System.out.println(Hello);
		
		String myName = "Cybertek " + " School ";
		System.out.println(myName);
		
		String Year = "This is " + 2019;
		System.out.println(Year);
		
		String name = "Muradil";
		System.out.println("My name is " + name);
		
		int cucumber = 3;
		int tomato = 5;
		System.out.println("cucumber is " + "$" + cucumber );
		System.out.println("tomato is " + "$" + tomato );
		
		
		String newStr =  " 100" + 10;
		System.out.println(newStr);
		
		System.out.println(1 + 2 + 3);
		System.out.println("1" + 2 + 3); // 123
		
		// System.out.println("1" + 1 - 3);  "11" - 3
		
		System.out.println(" Batch 12" + 1 + 2); // Batch 1212
		
		System.out.println(2 -1 + 1 + "4");  // 24
		
		System.out.println("Batch 12" + (1 + 2)); // Batch 123
		
		System.out.println(1 + "123" + 4 + 5);  // 112345
		
		System.out.println("123" + (4+5)); // 1239
		
		System.out.println(1 + ("1" + 2));  // 112
		
		System.out.println(1 + "123" + ( 4 + 5)); // 11239
		
		System.out.println(4 + 3 - (9 + 3));  // -5
		
		System.out.println((1 + 2) + "3");  // 33
		
		//System.out.println(  ( 1 + 2) - "3");
		
        String BookName = " I like the book called \' Game of Thrones \'";		
        System.out.println(BookName);
		
        String MyInfo = " My name is: \n\tCybertek";
        System.out.println(MyInfo);
        
        System.out.println('3' + 3); // 51 + 3 => 54
        
        System.out.println("3" + 3); // 33
        
        System.out.println(2 + 3); // 5
        
        System.out.println("2" + 3); // 23
        
        System.out.println('7' + 3);  // 58
        
        System.out.println('7' + "3"); //  73
        
        
        
		
	}

}
