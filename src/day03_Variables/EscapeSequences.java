package day03_Variables;

public class EscapeSequences {

	/*
		 \n: Next line, starts from a new line (or you can use \r)
		  
		 */
	
	public static void main(String[] args) {
		System.out.println(" Hello Cybertek \n Batch 12 students ");

       System.out.print(" Tomorrow is off \r ");		
       System.out.print(" True ");		
       System.out.print(" Flase \n");		
       //   \' prints single quote
       //   \" Prints double quote
       //  \: Backslash
       //   /: Frontslash
      
       
       System.out.print(" \t Class starts tomorrow ");		
       System.out.print(" \\muhtar ");		
       System.out.print(" \'Java' ");	
       System.out.println(" \\n");
       
       
	}

}
