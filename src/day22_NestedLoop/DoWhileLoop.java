package day22_NestedLoop;

public class DoWhileLoop {

	public static void main(String[] args) {

		for(int j =1; j <= 2; j++) {
			
			for (int i =1;  i <= j; i++) {
				System.out.print("*");
			}
			
			System.out.println();
			for(int a =1; a <= 5; a++) {
				
				for (int i =1;  i <= a; i++) {
					System.out.print("*");
	}
				System.out.println();
}
}
	}
}