package day21_WhileLoops;

public class ConvertingForToWhile {

	public static void main(String[] args) {

		for (int z=1;   z<=100; z++ ) {
			if(z%2 == 0)  {
				System.out.println(z );
			}
		}
		int x = 1;
		while (x <= 100) {
			if (x %2 == 0) {
			System.out.println(x + " ");
	}

}
	}
}
