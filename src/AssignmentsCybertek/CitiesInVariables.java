package AssignmentsCybertek;

public class CitiesInVariables {

	public static void main(String[] args) {

		int city1 = 2345673;
		int city2 = 5688944;
		int city3 = 16189445;
		
		System.out.println(city1);
		System.out.println(city2);
		System.out.println(city3);
		
		System.out.print(city1 + "\t");
		System.out.print(city2 + "\t");
		System.out.println(city3 + "\t");

		System.out.println(city1 + " " + city2 + " " + city3 );
		
		System.out.println(city1 + "\t" + city2 + "\t" + city3 );
		
		System.out.print(city1 + " ");
		System.out.print(city2 + " ");
		System.out.print(city3 + " ");  

		
	}

}
